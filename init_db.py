def main():
    if os.path.exists(DB_PATH):
        os.remove(DB_PATH)
        print(f"Removed existing {DB_PATH}")

    conn = sqlite3.connect(DB_PATH)
    conn.execute("PRAGMA journal_mode=WAL")
    conn.executescript(SCHEMA)

    for r in RECIPES:
        conn.execute(
            """INSERT INTO recipes
               (name, category, description, calories, servings, prep_time, cook_time,
                ingredients, steps, healthy_description, healthy_calories,
                healthy_ingredients, healthy_steps, healthy_benefits, health_score)
               VALUES (?,?,?,?,?,?,?,?,?,?,?,?,?,?,?)""",
            (
                r["name"], r["category"], r["description"],
                r["calories"], r["servings"], r["prep_time"], r["cook_time"],
                json.dumps(r["ingredients"]),
                json.dumps(r["steps"]),
                r["healthy_description"],
                r["healthy_calories"],
                json.dumps(r["healthy_ingredients"]),
                json.dumps(r["healthy_steps"]),
                json.dumps(r["healthy_benefits"]),
                r["health_score"],
            )
        )

    conn.commit()
    conn.close()
    print(f"✓ Created {DB_PATH} with {len(RECIPES)} recipes.")
    cats = {}
    for r in RECIPES:
        cats[r["category"]] = cats.get(r["category"], 0) + 1
    for cat, count in sorted(cats.items()):
        print(f"  {cat}: {count}")


if __name__ == "__main__":
    main()
