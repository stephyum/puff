# ingredient helper
def ing(us, metric, swap=False, swap_from_us=None, swap_from_metric=None, swap_reason=None):
    d = {"us": us, "metric": metric, "swap": swap}
    if swap:
        d["swap_from_us"]     = swap_from_us
        d["swap_from_metric"] = swap_from_metric
        d["swap_reason"]      = swap_reason
    return d

